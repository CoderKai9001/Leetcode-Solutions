class Solution {
public:
    vector<int> searchRange(vector<int>& nums, int target) {
        vector<int> result;
        int start = 0, mid, end = nums.size()-1, flag = 0;
        while(start <= end) {
            mid = (start+end)/2;
            if(nums[mid] == target && (mid == 0 || nums[mid-1] != target)) {
                result.push_back(mid);
                flag = 1;
                break;
            } else if(nums[mid] == target && nums[mid-1] == target) {
                end = mid-1;
            } else if(nums[mid] < target) {
                start = mid+1;
            } else if(nums[mid] > target) {
                end = mid-1;
            }
        }
        if(flag == 0) result.push_back(-1);
        flag = 0;
        start = 0;
        end = nums.size()-1;
        while(start <= end) {
            mid = (start+end)/2;
            if(nums[mid] == target && (mid == nums.size()-1 || nums[mid+1] != target)) {
                result.push_back(mid);
                flag = 1;
                break;
            } else if(nums[mid] == target && nums[mid+1] == target) {
                start = mid+1;
            } else if(nums[mid] < target) {
                start = mid+1;
            } else if(nums[mid] > target) {
                end = mid-1;
            }
        }
        if(flag == 0) result.push_back(-1);
        return result;
    }
};

int main() {
    Solution s;
    vector<int> nums = {5,7,7,8,8,10};
    int target = 8;
    vector<int> result = s.searchRange(nums, target);
    for(int i=0; i<result.size(); i++) {
        cout << result[i] << " ";
    }
    cout << endl;
    return 0;
}