class Solution {
public:
    string convert(string s, int numRows) {
        int n = s.size();
        if(n <= numRows || numRows == 1) return s;
        int jmp = (numRows-1)*2;
        int jmp1 = jmp, jmp2;
        vector<vector<char>> V(numRows);
        for(int i = 0; i < n; i+=jmp) {
            V[0].push_back(s[i]);
        }
        for(int i = numRows-1; i < n; i+=jmp) {
            V[numRows-1].push_back(s[i]);
        }
        for(int i = 1; i < numRows-1; i++) {
            jmp1 = jmp1-2;
            jmp2 = jmp - jmp1;
            int j = i;
            while(j < n) {
                V[i].push_back(s[j]);
                j+=jmp1;
                if(j >= n) break;
                V[i].push_back(s[j]);
                j+=jmp2;
            }
        }
        s = "";
        for(int i = 0; i < V.size(); i++) {
            for(int j = 0; j < V[i].size(); j++) {
                cout << V[i][j] << " ";
                s.append(1, V[i][j]);
            }
            cout << '\n';
        }
        cout << s << '\n';
        return s;
    }
};
