// ⠀⠀⠀⠀⠀⡄⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⡇⠀⠀⠀⠀⠀

class Solution {
public:

    vector<int> searchRow(vector<vector<int>>& matrix, int target) {
        int start = 0, mid, end = matrix.size()-1;
        vector<int> warn_dodge(2, 99);
        while(start <= end) {
            mid = (start+end)/2;
            if ((mid+1 == matrix.size() && matrix[mid][0] <= target)||(matrix[mid+1][0] > target && matrix[mid][0] <= target)) {
                return matrix[mid];
            } else if(mid == 0 && matrix[mid][0] > target) {
                vector<int> fail;
                fail.push_back(-10001);
                return fail;
            } else if(mid+1 == matrix.size() && target >= matrix[mid][0]) {
                return matrix[mid];
            } else if (matrix[mid+1][0] <= target) {
                start = mid+1;
            } else if (matrix[mid][0] > target) {
                end = mid-1;
            }
        }
        return warn_dodge;
    }

    bool searchMatrix(vector<vector<int>>& matrix, int target) {
        vector<int> req;
        if(matrix.size() == 0) return false;
        else if(matrix.size() == 1) req = matrix[0];
        else req = searchRow(matrix, target);
        int start = 0, mid, end = req.size()-1;
        while(start <= end) {
            mid = (start+end)/2;
            if(req[mid] == target) return true;
            else if(req[mid] < target) start = mid+1;
            else end = mid-1;
        }
        return false;
    }

    void printMatrix(vector<vector<int>>& matrix) {
        for(int i = 0; i < matrix.size(); i++) {
            for(int j = 0; j < matrix[i].size(); j++) {
                cout << matrix[i][j] << " ";
            }
            cout << endl;
        }
    }
};


int main() {
    Solution s;
    vector<vector<int>> matrix = {{1,3,5,7},{10,11,16,20},{23,30,34,60}};
    s.printMatrix(matrix);
    cout << s.searchMatrix(matrix, 3) << endl;
    return 0;
}